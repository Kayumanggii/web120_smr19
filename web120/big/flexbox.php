     <p>Flexbox is a technology that provides tools used by web designers and developers to build an efficient way to create complex layouts. This makes designing flexible responsive layouts much simpler and faster because you do not need to structure it by using float or positioning. Flexbox vertically centers the block of content inside the parent as well making all the children of the container adopt equal amounts of the available width and height.</p>

     <p>To use flexbox, you simply select what elements you would want to layout as flexible boxes by setting special values of display on the parent elements. To create layout article elements, set it on the section element and type flex next the display element. To create a layout inline with item as flexible boxes, display the value of inline-flex. A property called flex-direction that declares what direction the main axis runs through is also provided by flex-box. </p>
	
     <p>Flexbox has such a broader browser support system that most browsers do not require the usage of prefixes. Firefox is one of the browsers that has the best flexbox support and Safari was the last out of the major browsers to remove prefixes. When using flexbox, you do not need to worry about syntax because of the use of autoprefixer. Keeping track of vendor prefixes is a waste of time and effort so you should always automate it. Being able to center elements in a breeze is also one of the best attributes of flexbox. You would only need to use 3 declarations to achieve this perfectly.  Along with being able to easily center the elements, you can also easily manipulate the inline elements by simply aligning the elements using edge-to-edge. Lastly, the usage of flexbox simplifies complexity by allowing you to create a complex layout with such little declarations.</p>
	
     <p>To achieve using flexbox, you  must know your project, audience and desired results and fully understand that for some older browsers, you might still need to use alternatives. It is a wonderful modern tool to use in today’s world of web design.</p>


