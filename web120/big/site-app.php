<p>In this day and age, more than half of the global web traffic is done on mobile devices. As a result of this, it is very important for businesses to create a platform on the web that is easily accessible to consumers on mobile devices around the world. There is not a black and white answer to weather a responsive website is better than mobile applications and vice versa however there are definitely a few pros and cons to each.</p>
     
<p>A responsive designed website is a web page creation approach that makes use of flexible layouts, flexible images and cascading style sheet media queries. Responsive websites adopts to the visitor’s screen size and orientation which then changes the layout appropriately. This is a quicker process  and cost less than creating a mobile application. Most businesses tend to need responsive mobile websites to keep up with their competitions since this is a way to give a user the perfect experience without having to manually resize anything to view its content regardless of what device they are using. Nonetheless, as they say, you get what you pay for. Responsive websites are not as fast and take longer to navigate compared to mobile applications.</p> 
	
<p>A mobile application is what most of us in the modern world call an app, it is a kind of application software designed to run on a mobile device such as a tablet or a smartphone. A mobile application is easier and faster to navigate than responsive websites however it takes longer to create and costs much more that responsive websites. Along with being easier and faster to navigate, mobile applications are great for brand awareness and marketing. It places the brand front and center of each user and deliver information back to businesses about user behaviors which helps developmental decisions in the future. As great mobile applications are, there are a few things that are not so great about them such as being hard to build and the time it takes to build one. When an application is built poorly, it can go against the businesses brand because when a user sees this, it will make the business look amateur. So, when most business choose to go with building a mobile application, they need to make sure that they have enough money to invest into building it so that it is successful.</p>
	
<p>With reviewing the pros and cons of responsive websites and mobile applications, it was not a matter of responsive websites being better than mobile applications and vice versa but rather which one fits best for what you will use it for. It is best to do your research and examine your budget while determining which platform would work best for you or your business.</p>

